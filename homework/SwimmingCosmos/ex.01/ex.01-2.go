package main

import "fmt"

func main() {
	a := 3
	b := 5
	fmt.Println("a + b = ", a+b) // 8
	fmt.Println("a - b = ", a-b) // -2
	fmt.Println("a * b = ", a*b) // 15
	fmt.Println("a / b = ", a/b) // 0
	fmt.Println("a % b = ", a%b) // 3
	fmt.Println("aの2乗 = ", a*a)  // 9
	// :=を使って変数を宣言することもできます。varでもいける
}
